package maran.java.base.estruturadecontroles;

public class While01 {
	
	public static void main(String[] args) {
		
		System.out.println("+-------+");
		System.out.println("| while |");
		System.out.println("+-------+");
		System.out.println();
		
		int contador = 0;
		while(contador <= 10) {
			System.out.println(contador);
			contador++;
		}
		
	}

}
