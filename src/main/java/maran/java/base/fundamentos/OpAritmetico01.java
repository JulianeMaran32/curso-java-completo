package maran.java.base.fundamentos;

public class OpAritmetico01 {

	public static void main(String[] args) {
		
		System.out.println("2 + 3 = " + (2 + 3));
		System.out.println();
		
		var x = 34.56;
		double y = 2.2;
		
		System.out.println("x + y = " + (x + y));
		System.out.println("x - y = " + (x - y));
		System.out.println("x * y = " + (x * y));
		System.out.println("x / y = " + (x / y));
		System.out.println();
		
		int a = 8;
		int b = 3;
		System.out.println("a + b = " + (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b));
		System.out.println("a / (double) b = " + (a / (double) b));		
		System.out.println();
		
		System.out.println("a % b = " + (a % b));
		System.out.println("8 % 3 = " + (8 % 3));
		System.out.println();
		
		System.out.println("x + y - a * b = " + (x + y - a * b));
		System.out.println();
		

	}

}
